a = int(input("Ingresa un número\n"))
b = 50
#Programa que verifica si un número es mayor que 50
if a > b:
    print("Tu número " + str(a) + " es mayor que " + str(b))
elif a == b:
    print("Tu número " + str(a) + " es igual que " + str(b))
else:
    print("Tu número " + str(a) + " no es mayor que " + str(b))
